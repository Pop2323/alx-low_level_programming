#include "3-calc.h"

/**
 * op_add - the sum of two ints a and b
 *
 * @a: 1st int
 * @b: 2nd int
 *
 * Return: return the sum
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - the difference of two ints a and b
 *
 * @a: 1st int
 * @b: 2nd int
 *
 * Return: return the difference
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - the product of two ints a and b
 *
 * @a: 1st int
 * @b: 2nd int
 *
 * Return: return the product
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - the result of the division of two ints a and b
 *
 * @a: 1st int
 * @b: 2nd int
 *
 * Return: return the result of the division
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - the result of  the remainder of the division of two ints a and b
 *
 * @a: 1st int
 * @b: 2nd int
 *
 * Return: return  the remainder of the division
*/

int op_mod(int a, int b)
{
	return (a % b);
}
